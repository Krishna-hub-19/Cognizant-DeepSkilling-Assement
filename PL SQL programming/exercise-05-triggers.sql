-- Scenario 2: Maintain an audit log for all transactions.
CREATE TABLE AuditLog (
    AuditID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TransactionID NUMBER,
    AccountID NUMBER,
    TransactionType VARCHAR2(20),
    Amount NUMBER(10,2),
    TransactionDate DATE,
    AuditDate DATE
);

-- Trigger
CREATE OR REPLACE TRIGGER LogTransaction
AFTER INSERT
ON Transactions
FOR EACH ROW
BEGIN
    INSERT INTO AuditLog (
        TransactionID,
        AccountID,
        TransactionType,
        Amount,
        TransactionDate,
        AuditDate
    )
    VALUES (
        :NEW.TransactionID,
        :NEW.AccountID,
        :NEW.TransactionType,
        :NEW.Amount,
        :NEW.TransactionDate,
        SYSDATE
    );
END;
/

-- Test
